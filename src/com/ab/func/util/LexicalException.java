package com.ab.func.util;

public class LexicalException extends BaseException {

	public LexicalException() {
	}

	public LexicalException(String message, Throwable cause) {
		super(message, cause);
	}

	public LexicalException(Throwable cause) {
		super(cause);
	}

	public LexicalException(String cause) {
		super(cause);
	}

}
